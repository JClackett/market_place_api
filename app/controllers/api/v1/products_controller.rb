class Api::V1::ProductsController < ApplicationController
  respond_to :json

  
# ---------------------------------------------------------------------------------------------------------------------------------------------------------------
# GET : INDEX
# ---------------------------------------------------------------------------------------------------------------------------------------------------------------
 	
 	def index
 		respond_with Product.all
 	end

# ---------------------------------------------------------------------------------------------------------------------------------------------------------------
# GET : SHOW
# ---------------------------------------------------------------------------------------------------------------------------------------------------------------
 
	def show
		respond_with Product.find(params[:id])
	end


end